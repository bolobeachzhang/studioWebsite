package cn.edu.cdu.lab.model;

import java.sql.Date;


public class Thesis {
	private Integer id;
	private String headLine;// 论文标题
	private Integer type;// 论文类型 一般期刊=1，核心期刊=2，EI=4，SIC=8
	private String journalTitle;// 期刊名
	private Date time;// 发表时间
	private String author;// 论文作者
	private String teacherId;// 上传教师Id号
	private String url;// 论文的下载地址
	private String size;// 论文大小
	private String format;//格式
	private Integer downloads;//下载次数
	private String digest;//论文摘要
	private Integer downrole;//下载权限 全部人员=1，学生、老师和注册人员=2，学生和老师=4，老师=8(管理员都有权下载)
	private Integer checkState; //是否被审核,未审核=0，已审核=1

	public Integer getCheckState() {
		return checkState;
	}

	public void setCheckState(Integer checkState) {
		this.checkState = checkState;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getHeadLine() {
		return headLine;
	}

	public void setHeadLine(String headLine) {
		this.headLine = headLine;
	}

	public String getJournalTitle() {
		return journalTitle;
	}

	public void setJournalTitle(String journalTitle) {
		this.journalTitle = journalTitle;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getTeacherId() {
		return teacherId;
	}

	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public Integer getDownloads() {
		return downloads;
	}

	public void setDownloads(Integer downloads) {
		this.downloads = downloads;
	}

	public String getDigest() {
		return digest;
	}

	public void setDigest(String digest) {
		this.digest = digest;
	}

	public Integer getDownrole() {
		return downrole;
	}

	public void setDownrole(Integer downrole) {
		this.downrole = downrole;
	}
}
