package cn.edu.cdu.lab.model;


public class AboutUs {
	private Integer id;		//表的自增长id号
	private String copyRight;	//版权所有
	private String address;		//地址
	private String postCode;	//邮编
	private String phoneNuber;	//联系电话
	private String fax;			//传真
	private String webSite;		//网址
	private String icpRecode;	//IPC备案
	private String mailBox;		//邮箱
	public String getMailBox() {
		return mailBox;
	}
	public void setMailBox(String mailBox) {
		this.mailBox = mailBox;
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCopyRight() {
		return copyRight;
	}
	public void setCopyRight(String copyRight) {
		this.copyRight = copyRight;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getPhoneNuber() {
		return phoneNuber;
	}
	public void setPhoneNuber(String phoneNuber) {
		this.phoneNuber = phoneNuber;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getWebSite() {
		return webSite;
	}
	public void setWebSite(String webSite) {
		this.webSite = webSite;
	}
	public String getIcpRecode() {
		return icpRecode;
	}
	public void setIcpRecode(String icpRecode) {
		this.icpRecode = icpRecode;
	}
}
 