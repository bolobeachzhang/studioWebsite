package cn.edu.cdu.lab.vo;

import java.util.List;

import cn.edu.cdu.lab.model.Teacher;

/**
 * @ClassName: Research 
 * @Description: 用于前台研究队伍的岗位数据传递
 * @author:chenliang
 * @date：2013-03-24
 */
public class Post {
	private List<Teacher> academicLeaders; //学术带头人
	private List<Teacher> directors; //主任
	private List<Teacher> offices; //办公室
	private List<Teacher> leaders; //组长
	private List<Teacher> teachers; //教师
	public List<Teacher> getAcademicLeaders() {
		return academicLeaders;
	}
	public void setAcademicLeaders(List<Teacher> academicLeaders) {
		this.academicLeaders = academicLeaders;
	}
	public List<Teacher> getDirectors() {
		return directors;
	}
	public void setDirectors(List<Teacher> directors) {
		this.directors = directors;
	}
	public List<Teacher> getOffices() {
		return offices;
	}
	public void setOffices(List<Teacher> offices) {
		this.offices = offices;
	}
	public List<Teacher> getLeaders() {
		return leaders;
	}
	public void setLeaders(List<Teacher> leaders) {
		this.leaders = leaders;
	}
	public List<Teacher> getTeachers() {
		return teachers;
	}
	public void setTeachers(List<Teacher> teachers) {
		this.teachers = teachers;
	}
	
}
